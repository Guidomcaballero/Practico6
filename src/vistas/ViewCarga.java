    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        DirectorioTelefonico dt = Escritorio.getDr();
        Cliente c;
        boolean eLanzada = false;
        long aux=0L;
        try{
            aux = Long.valueOf(jtDNI.getText());
        }catch(Exception e){
            eLanzada=true;
            jtDNI.setText("");
        }
        if(dt.buscarDni(aux)!= null && !eLanzada){
            c = dt.buscarDni(aux);
            jtNombre.setText(c.getNombre());
            jtApellido.setText(c.getApellido());
            jtDomicilio.setText(c.getDireccion());
            jtCiudad.setText(c.getCiudad());
            jtTelefono.setText(""+dt.buscaClave(aux));
            jtNombre.setEditable(false);
            jtApellido.setEditable(false);
            jtDomicilio.setEditable(false);
            jtCiudad.setEditable(false);
            jtTelefono.setEditable(false);
            jbAceptar.setEnabled(false);
        }else{
            if(!eLanzada){
                JOptionPane.showMessageDialog(null,"Por favor complete los datos faltantes");
                jtNombre.setEditable(true);
                jtApellido.setEditable(true);
                jtDomicilio.setEditable(true);
                jtCiudad.setEditable(true);
                jtTelefono.setEditable(true);
                jbAceptar.setEnabled(true);
            }else
                JOptionPane.showMessageDialog(null,"Usted ingres√≥ un caracter incorrecto, por favor ingrese un numero v√°lido");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed



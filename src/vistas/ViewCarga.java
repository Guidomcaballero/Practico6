    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        DirectorioTelefonico dt = Escritorio.getDr();
        Long key,dni;
        boolean eLanza=false;
        String name="",lastname="",city="",address="";
        try{
            dni = Long.valueOf(jtDNI.getText());
            key = Long.valueOf(jtTelefono.getText());
            name = jtNombre.getText();
            lastname = jtApellido.getText();
            city = jtCiudad.getText();
            address = jtDomicilio.getText();
        }catch(Exception e){
            eLanza=true;
        }
        if(!eLanza){
            dni = Long.valueOf(jtDNI.getText());
            key = Long.valueOf(jtTelefono.getText());
            Cliente c = new Cliente(dni,name,lastname,city,address);
            dt.agregarCliente(key, c);
            Escritorio.setDr(dt);
            this.jbBuscarActionPerformed(evt);
            this.jbNuevoActionPerformed(evt);
        }else{
            JOptionPane.showMessageDialog(null,"Los campos de DNI Y NUMERO TELEFONICO solo admiten caracteres de tipo num√©rico.");
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
        }
    }//GEN-LAST:event_jbAceptarActionPerformed
